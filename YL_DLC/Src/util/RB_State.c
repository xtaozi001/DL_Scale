static RB_STATE_tData s_pParams;		//!< State data


//==================================================================================================
//  G L O B A L   F U N C T I O N   I M P L E M E N T A T I O N
//==================================================================================================

//--------------------------------------------------------------------------------------------------
// RB_STATE_Initialize
//--------------------------------------------------------------------------------------------------
//! \brief	Initialize the module
//!
//! \return	none
//--------------------------------------------------------------------------------------------------
void RB_STATE_Initialize(void)
	{
	}


//--------------------------------------------------------------------------------------------------
// RB_STATE_SetTDParams
//--------------------------------------------------------------------------------------------------
//! \brief	Set type data parameter
//!
//! \param	pParams		parameters to set
//! \return	none
//--------------------------------------------------------------------------------------------------
void RB_STATE_SetTDParams(const RB_STATE_tData *pParams)
	{
	s_pParams = *pParams;
	}


//--------------------------------------------------------------------------------------------------
// RB_STATE_GetOperatingMode
//--------------------------------------------------------------------------------------------------
//! \brief	This function gets the operating mode
//!
//! \return	Value of actual operating mode
//--------------------------------------------------------------------------------------------------
RB_STATE_tOperatingMode RB_STATE_GetOperatingMode(void)
	{
	return(s_pParams.mode);
	}


